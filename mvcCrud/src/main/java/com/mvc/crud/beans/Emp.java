private float SALARY;    
private String DESIGNATION;
public int getID() {
	return ID;
}
public void setID(int iD) {
	ID = iD;
}
public String getNAME() {
	return NAME;
}
public void setNAME(String nAME) {
	NAME = nAME;
}
